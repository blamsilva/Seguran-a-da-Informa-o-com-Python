# - TCP - Transmission Control Protocol  - é um dos protocolos de comunicação, que dão suporte a rede global
# internet, verificando se os dados são enviados na sequência correta e sem erros.

# - UDP - User Datagram Protocol é um protocolo simples da camada de transporte que permite que a aplicação envie um
# datagrama dentro num pacote IPv4 ou IPv6 a um destino, porém sem qualquer tipo de garantia que o pacote chegue corretamente.